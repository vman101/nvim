-- Function to toggle mouse
function ToggleMouse()
    local mouse_enabled = vim.o.mouse

    -- Toggle mouse state
    if mouse_enabled == 'a' then
        vim.o.mouse = ''
    else
        vim.o.mouse = 'a'
    end

    print('Mouse ' .. (mouse_enabled == 'a' and 'disabled' or 'enabled'))
end

